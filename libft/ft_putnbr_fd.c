#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	c;

	if (n < 0)
		write(fd, "-", 1);
	else
		n *= -1;
	i = 1;
	while (n / i <= -10)
		i *= 10;
	while (i > 0)
	{
		c = n / i * -1 + '0';
		write(fd, &c, 1);
		n %= i;
		i /= 10;
	}
}
