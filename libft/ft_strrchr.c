char	*ft_strrchr(const char *s, char c)
{
	int	i;
	int	ret;

	i = 0;
	ret = -1;
	while (*(s + i) != '\0')
	{
		if (*(s + i) == c)
			ret = i;
		i++;
	}
	if (ret == -1)
		ret = i;
	return ((char *)s + ret);
}
